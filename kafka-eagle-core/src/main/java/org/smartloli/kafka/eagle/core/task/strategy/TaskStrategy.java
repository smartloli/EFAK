package org.smartloli.kafka.eagle.core.task.strategy;

import org.smartloli.kafka.eagle.common.protocol.BaseProtocol;

/**
 * Define a distribution strategy for master generation
 *
 * @author smartloli.
 * <p>
 * Created by Sep 11, 2020
 */
public class TaskStrategy extends BaseProtocol {

    private int id;
    private String name;
    private int price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
